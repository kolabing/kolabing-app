/// Kolabing spacing scale
///
/// Consistent spacing values used throughout the application.
/// Based on a 4px base unit with named semantic values.
abstract final class KolabingSpacing {
  /// Extra extra extra small - 2px
  static const double xxxs = 2;

  /// Extra extra small - 4px
  static const double xxs = 4;

  /// Extra small - 8px
  static const double xs = 8;

  /// Small - 12px
  static const double sm = 12;

  /// Medium - 16px (base unit)
  static const double md = 16;

  /// Large - 24px
  static const double lg = 24;

  /// Extra large - 32px
  static const double xl = 32;

  /// Extra extra large - 48px
  static const double xxl = 48;

  /// Extra extra extra large - 64px
  static const double xxxl = 64;
}
